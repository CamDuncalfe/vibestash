async function main() {
  // Check for existing slugs to avoid duplicates
  const { data: existing } = await supabase
    .from('products')
    .select('slug')
    .in('slug', products.map(p => p.slug));
  
  const existingSlugs = new Set((existing || []).map(p => p.slug));
  const newProducts = products.filter(p => !existingSlugs.has(p.slug));
  
  console.log(`Existing products to skip: ${existingSlugs.size}`);
  console.log(`New products to add: ${newProducts.length}`);
  
  if (newProducts.length === 0) {
    console.log('Nothing new to add.');
    return;
  }

  // Insert in batch
  const { data, error } = await supabase
    .from('products')
    .insert(newProducts)
    .select('id, title, slug');
  
  if (error) {
    console.error('Insert error:', error.message);
    return;
  }
  
  console.log(`\nSuccessfully added ${data.length} products:`);
  data.forEach(p => console.log(`  ✅ ${p.title} (${p.slug})`));
  
  // Show new totals
  const { count } = await supabase.from('products').select('*', { count: 'exact', head: true }).eq('approved', true);
  console.log(`\nTotal approved products: ${count}`);
}

main().catch(console.error);
